#[derive(Debug, Clone, PartialEq)]
pub struct Token {
    pub line: u128,
    pub typ: TokenType,
}

#[derive(Debug, Clone, PartialEq)]
pub enum TokenType {
    LeftParen,
    RightParen,
    LeftBrace,
    RightBrace,
    Comma,
    Dot,
    Minus,
    Plus,
    Semicolon,
    Slash,
    Star,
    Pow,
    Equal,
    Grater,
    GraterEqual,
    Less,
    LessEqual,
    Identifier(String),
    String(String),
    Number(f32),
    And,
    Or,
    If,
    Else,
    Is,
    Not,
    Fun,
    For,
    Nil,
    Print,
    Return,
    True,
    False,
    Var,
    Const,
    While,
    Break,
    Eof,
}